   <h1 class="h1">🎲 Comment jouer aux échecs ?</h1>
   <p class="ol">1️⃣ Le plateau : Une grille de 8x8 cases, avec des pièces blanches et noires. 2️⃣ Les pièces : Chaque joueur commence avec 16 pièces :</p>
   <ol class="ol">
    <ul>
        <li>1 Roi 👑 (se déplace d'une case dans toutes les directions)</li>
        <li>1 Dame 👸 (se déplace en ligne droite dans toutes les directions)</li>
        <li>2 Tours 🏰 (se déplacent en ligne droite, horizontalement ou verticalement)</li>
        <li>2 Fous ⛪ (se déplacent en diagonale)</li>
        <li>2 Cavaliers 🐴 (se déplacent en "L" et peuvent sauter par-dessus les autres pièces)</li>
        <li>8 Pions ⚪ (se déplacent d'une case vers l'avant, sauf au premier coup où ils peuvent avancer de deux cases)</li>
    </ul>
   </ol>
   <p class="ol">3️⃣ Objectif : Mettre le roi adverse en échec et mat, c'est-à-dire l'empêcher de bouger sans être capturé. 4️⃣ Déroulement :</p>
   <ol class="ol">
    <ul>
        <li>Les joueurs jouent à tour de rôle.</li>
        <li>Chaque pièce a une façon spécifique de se déplacer.</li>
        <li>On peut capturer une pièce adverse en se déplaçant sur sa case.</li>
        <li>Certaines règles spéciales existent, comme le roque, la promotion du pion et la prise en passant.</li>
    </ul>
   </ol>
   <p class="ol">5️⃣ <u> Fin de partie :</u></p>
   <ol class="ol">
    <ul>
        <li>Échec et mat : Un joueur gagne si le roi adverse ne peut plus bouger sans être capturé.</li>
        <li>Pat : Match nul si un joueur ne peut plus jouer mais n'est pas en échec.</li>
        <li>Autres cas de match nul : Si les deux joueurs n'ont plus assez de pièces pour gagner ou si la même position se répète plusieurs fois.</li>
    </ul>
   </ol>
