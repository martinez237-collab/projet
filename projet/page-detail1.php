    <h1 class="h1">🎲 Comment jouer aux dominos ?</h1>
    <p class="ol">1️⃣ Matériel : Un jeu de 28 dominos, chacun divisé en deux cases avec des points allant de 0 à 6. 2️⃣ Nombre de joueurs : De 2 à 4 joueurs. 3️⃣ Objectif : Être le premier à placer tous ses dominos ou à bloquer l’adversaire. 4️⃣ Déroulement :</p>
    <ol class="ol">
        <ul>
            <li>Chaque joueur pioche un certain nombre de dominos (7 à 2 joueurs, 6 à 3 ou 4 joueurs)</li>
            <li>Le joueur qui possède le double le plus élevé commence la partie</li>
            <li>À tour de rôle, chaque joueur pose un domino en l’alignant avec un autre ayant le même nombre de points sur une de ses cases</li>
            <li>Si un joueur ne peut pas jouer, il pioche un domino et passe son tour</li>
        </ul>
    </ol>
     <h2>5️⃣ <u>Fin de partie </u></h2>
    <ol class="ol">
        <ul>
            <li>
                Victoire : Le premier joueur à placer tous ses dominos gagne
            </li>
            <li>
                Match bloqué : Si aucun joueur ne peut jouer, celui qui a le moins de points sur ses dominos restants gagne.
            </li>
        </ul>
    </ol>
    <h2>🏆<u> Astuces pour bien jouer </u></h2>
    <p class="ol">✅ Commence avec un double élevé pour avoir plus d’options. ✅ Anticipe les coups pour bloquer l’adversaire. ✅ Garde des dominos variés pour éviter d’être bloqué.</p>
